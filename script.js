const linksButton = document.querySelector(".svg-container");

linksButton.onclick = toggleLinks;

function toggleLinks() {
    const shown = document.querySelector(".show");
    if(shown) {
        // hide links
        document.querySelector(".links-container").classList.remove("show"); 
        document.querySelector(".links-container").classList.add("hide"); 
    }
    else {
        // show links
        document.querySelector(".links-container").classList.add("show");
        document.querySelector(".links-container").classList.remove("hide");
    }
}

